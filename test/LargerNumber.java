public class LargerNumber {
	/** Returns the larger number of x and y. */
	public static int Larger(int x, int y) {
		if (x >= y) {
			return x;
		}
		return y;
		
	}
	public static void main(String[] args) {
		System.out.println(Larger(1, 5));
	}
}
